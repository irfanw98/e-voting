        Pusher.logToConsole = true;

        var pusher = new Pusher('{{ config('notifikasi.pusher_key') }}', {
          cluster: '{{ config('notifikasi.pusher_cluster') }}'
        });

        var channel = pusher.subscribe('my-channel');
          channel.bind('my-event', function(data) {
            var contentNotif = $('.dropdown .dropdown-list-content').html();
            var notifikasi = $('.dropdown .countNotif').html();
            var notifikasiCount = parseInt(notifikasi);
            var html = '';

            if(data){

                html += '<a href="#" class="dropdown-item dropdown-item-unread"><div class="dropdown-item-desc">'+ data.message +'</div></a>';

                notifikasiCount +=1;
                $('.dropdown .countNotif').text(notifikasiCount);
                var notifikasiBaru = html + contentNotif;
                $('.dropdown .dropdown-list-content').html(notifikasiBaru);
            } 
          // alert(JSON.stringify(data));
        });
